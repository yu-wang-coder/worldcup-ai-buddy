export function getPlayers() {
  return players;
}

export function getPlayerById(id) {
  return players.find((p) => p.id === id);
}

export function getPlayersByTeam(teamId) {
  return players.filter((p) => p.teamId === teamId);
}

export function getStarPlayers() {
  return players.filter((p) => p.isStarPlayer);
}

export function getPlayersByPosition(position) {
  return players.filter((p) => p.position === position);
}

export function getTopGoalscorers(count = 10) {
  return [...players].sort((a, b) => b.goals - a.goals).slice(0, count);
}

export function getPlayersByTeamName(teamName) {
  return players.filter((p) => p.team === teamName);
}

export function searchPlayers(query) {
  const q = query.toLowerCase();
  return players.filter(
    (p) =>
      p.name.toLowerCase().includes(q) ||
      p.team.toLowerCase().includes(q) ||
      p.position.toLowerCase().includes(q) ||
      p.club.toLowerCase().includes(q)
  );
}
