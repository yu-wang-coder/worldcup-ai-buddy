export function getTeams() {
  return teams;
}

export function getTeamById(id) {
  return teams.find((t) => t.id === id);
}

export function getTeamByName(name) {
  return teams.find((t) => t.name === name);
}

export function getTeamsByConfederation(confederation) {
  return teams.filter((t) => t.confederation === confederation);
}

export function getTopRankedTeams(count = 10) {
  return [...teams].sort((a, b) => a.rank - b.rank).slice(0, count);
}

export function searchTeams(query) {
  const q = query.toLowerCase();
  return teams.filter(
    (t) =>
      t.name.toLowerCase().includes(q) ||
      t.country.toLowerCase().includes(q) ||
      t.shortName.toLowerCase().includes(q) ||
      t.nickname.toLowerCase().includes(q)
  );
}
